    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
            
        // If the timer has not run out and no radio button has been selected
        
        if (quizTimer.isRunning() && answerSelected() == null) {
        
            quizTimer.stop();
            javax.swing.JOptionPane.showMessageDialog(this, errorDialogLabel, "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            quizTimer.start();
            return;
        
        }
        
        checkAnswer();
        
        nextCounter++;
        
        if (nextCounter == allQuestions.size()) {
            
            saveScore();
            dispose();
            quizTimer.stop();
            new ReportSheet(correctAnswers, allQuestions.size()).setVisible(true);
            return;
            
        }
        
        questionLabel.setText(allQuestions.get(nextCounter)[0]);
        answerRadioButton1.setText(allQuestions.get(nextCounter)[1]);
        answerRadioButton2.setText(allQuestions.get(nextCounter)[2]);
        answerRadioButton3.setText(allQuestions.get(nextCounter)[3]);
        answerRadioButton4.setText(allQuestions.get(nextCounter)[4]);

        // Reset buttons
            
        resetRadioButtons();
        
        // Restart timer
        
        quizTimer.restart();
        incrementTimer.restart();
        
    }//GEN-LAST:event_nextButtonActionPerformed

    private void saveScore() {
    
        Path examineeQuizFolder = Paths.get(System.getProperty("user.home"), "QuizApplication", "Users", "Examinees", accountName, quizName);
            
        try {
            
            if (Files.notExists(examineeQuizFolder))
                Files.createDirectories(examineeQuizFolder);
            
        }
            
        catch(IOException x) {
            
            System.err.format("IOException: %s%n", x); 
            
        }
            
        try(BufferedWriter overwriteScore = Files.newBufferedWriter(examineeQuizFolder.resolve("score.log"), StandardOpenOption.CREATE);
                BufferedWriter overwriteAttempts = Files.newBufferedWriter(examineeQuizFolder.resolve("attempts.log"), StandardOpenOption.CREATE);
                BufferedReader readScore = Files.newBufferedReader(examineeQuizFolder.resolve("score.log"));
                BufferedReader readAttempts = Files.newBufferedReader(examineeQuizFolder.resolve("attempts.log"))) {
            
            double CorrectAnswersPercentage = (correctAnswers.doubleValue() / allQuestions.size()) * 100;
            String oldScore = readScore.readLine();
            if (oldScore == null || (CorrectAnswersPercentage > Double.parseDouble(oldScore)))
                overwriteScore.write(Double.toString(CorrectAnswersPercentage));
            
            String oldAttempts = readAttempts.readLine();
            if (oldAttempts == null)
                overwriteAttempts.write(Integer.toString(1));
            
            else {
            
                Integer newAttempts = Integer.parseInt(oldAttempts) + 1;
                overwriteAttempts.write(newAttempts.toString());
            
            }
        }
            
        catch(IOException x) {
            
            System.err.format("IOException: %s%n", x); 
            
        }
    }
    
    private void checkAnswer() {

        if (answerSelected() != null && answerSelected().equals(allQuestions.get(nextCounter)[5])) {
        
            correctAnswers++;
        
        }
    
    }
    
