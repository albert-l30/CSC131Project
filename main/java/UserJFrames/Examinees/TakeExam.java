    private void confirmQuizNameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmQuizNameButtonActionPerformed
        
        if (quizNameTextField.getText().equals(""))
            return;
        
        quizName = quizNameTextField.getText();
        Path quizFolder = null;
        
        try {
        
            quizFolder = Paths.get(System.getProperty("user.home"), "QuizApplication", "Quizzes", quizName);
            if(Files.notExists(quizFolder)) {
            
                topLabel.setText("The quiz with the name you entered does not exist.");
                return;
            
            }
        }
        
        catch(InvalidPathException x) {
        
            topLabel.setText("The quiz name you entered is invalid. Please enter a valid quiz name.");
            return;
        
        }
        
        try (BufferedReader readQuantity = Files.newBufferedReader(quizFolder.resolve("quantity.log")); 
                BufferedReader readQuestions = Files.newBufferedReader(quizFolder.resolve("questions.log")); 
                BufferedReader readAnswers = Files.newBufferedReader(quizFolder.resolve("answers.log"))) {

            allQuestions = new java.util.ArrayList<String[]>(Integer.parseInt(readQuantity.readLine()));
            String[] eachQuestion = new String[6];
            String nextQuestion = readQuestions.readLine();
            
            while (nextQuestion != null) {
            
                eachQuestion[0] = nextQuestion;
                
                // Choices for the question
                
                eachQuestion[1] = readQuestions.readLine();
                eachQuestion[2] = readQuestions.readLine();
                eachQuestion[3] = readQuestions.readLine();
                eachQuestion[4] = readQuestions.readLine();
                
                // Correct answer for the question
                
                eachQuestion[5] = readAnswers.readLine();
                
                allQuestions.add(eachQuestion);
                
                eachQuestion = new String[6];                                   // Have to create new array object and assign it to eachQuestion variable, or else eachQuestion variable will reference the same array until the end of file
                nextQuestion = readQuestions.readLine();
            
            }
            
            java.util.Collections.shuffle(allQuestions);

        }
        
        catch (IOException x) {
        
            System.err.format("IOException: %s%n", x);
        
        }
        
        // Set up initial questions in window 
        
        questionLabel.setText(allQuestions.getFirst()[0]);
        answerRadioButton1.setText(allQuestions.getFirst()[1]);
        answerRadioButton2.setText(allQuestions.getFirst()[2]);
        answerRadioButton3.setText(allQuestions.getFirst()[3]);
        answerRadioButton4.setText(allQuestions.getFirst()[4]);
        
        topLabel.setText("");
        quizNameTextField.setEnabled(false);
        confirmQuizNameButton.setEnabled(false);
        nextButton.setEnabled(true);
        quizTimer = new javax.swing.Timer(10000, new java.awt.event.ActionListener() {
            
            public void actionPerformed(java.awt.event.ActionEvent evt) {
            
                quizTimerActionPerformed(evt);
            
            }
            
        });
        
        
        incrementTimer = new javax.swing.Timer(1000, new java.awt.event.ActionListener() {
            
            public void actionPerformed(java.awt.event.ActionEvent evt) {
            
                incrementTimerActionPerformed(evt);
            
            }
            
        });
                
        topLabel.setText("Timer: " + Double.toString(quizTimer.getDelay() / (double) 1000));
        quizTimer.start();
        incrementTimer.start();
        
    }//GEN-LAST:event_confirmQuizNameButtonActionPerformed

