    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed

        Path questionsFile = Paths.get(System.getProperty("user.home"), "QuizApplication", "Quizzes", "Quiz1", "questions.log");
        try (BufferedReader reader = Files.newBufferedReader(questionsFile)) {
            
            nextCounter++;
            if (nextCounter == Integer.parseInt(reader.readLine()))             // reads first line of file and compares it to nextCounter
                dispose();
            
            // Skip questions and choices (for those questions) already given
            
            for (int i = 0; i < nextCounter * 5; i++) {
            
                reader.readLine(); 
            
            }
            
            questionLabel.setText(reader.readLine());
            answerRadioButton1.setText(reader.readLine());
            answerRadioButton2.setText(reader.readLine());
            answerRadioButton3.setText(reader.readLine());
            answerRadioButton4.setText(reader.readLine());

            // Reset buttons
            
            resetRadioButtons();
            
        }

        catch(IOException x) {

            System.err.format("IOException: %s%n", x);

        }
        
    }//GEN-LAST:event_nextButtonActionPerformed

