    private void answerClicked(int answer) {
    
        switch(answer) {
            
            case 1:
                answerRadioButton2.setEnabled(false);
                answerRadioButton3.setEnabled(false);
                answerRadioButton4.setEnabled(false);
                break;
            case 2:
                answerRadioButton1.setEnabled(false);
                answerRadioButton3.setEnabled(false);
                answerRadioButton4.setEnabled(false);
                break;
            case 3:
                answerRadioButton1.setEnabled(false);
                answerRadioButton2.setEnabled(false);
                answerRadioButton4.setEnabled(false);
                break;
            default:
                answerRadioButton1.setEnabled(false);
                answerRadioButton2.setEnabled(false);
                answerRadioButton3.setEnabled(false);
                
        }
    
    }
    
    private void answerUnclicked(int answer) {
    
        switch(answer) {
            
            case 1:
                answerRadioButton2.setEnabled(true);
                answerRadioButton3.setEnabled(true);
                answerRadioButton4.setEnabled(true);
                break;
            case 2:
                answerRadioButton1.setEnabled(true);
                answerRadioButton3.setEnabled(true);
                answerRadioButton4.setEnabled(true);
                break;
            case 3:
                answerRadioButton1.setEnabled(true);
                answerRadioButton2.setEnabled(true);
                answerRadioButton4.setEnabled(true);
                break;
            default:
                answerRadioButton1.setEnabled(true);
                answerRadioButton2.setEnabled(true);
                answerRadioButton3.setEnabled(true);
                
        }
    
    }
    
    private void resetRadioButtons() {
    
        if (answerRadioButton1.isSelected()) {
        
            // Enable check boxes
            
            answerRadioButton2.setEnabled(true);
            answerRadioButton3.setEnabled(true);
            answerRadioButton4.setEnabled(true);
            
            // Deselect check box 1
            
            answerRadioButton1.setSelected(false);
        
        }
        
        else if (answerRadioButton2.isSelected()) {
        
            // Enable check boxes
            
            answerRadioButton1.setEnabled(true);
            answerRadioButton3.setEnabled(true);
            answerRadioButton4.setEnabled(true);
            
            // Deselect check box 2
            
            answerRadioButton2.setSelected(false);
            
        }
        
        else if (answerRadioButton3.isSelected()) {
        
            // Enable check boxes
            
            answerRadioButton1.setEnabled(true);
            answerRadioButton2.setEnabled(true);
            answerRadioButton4.setEnabled(true);
            
            // Deselect check box 3
            
            answerRadioButton3.setSelected(false);
        
        } 
        
        else {
        
            // Enable check boxes
            
            answerRadioButton1.setEnabled(true);
            answerRadioButton2.setEnabled(true);
            answerRadioButton3.setEnabled(true);
            
            // Deselect check box 4
            
            answerRadioButton4.setSelected(false);
        
        }
    }
    
